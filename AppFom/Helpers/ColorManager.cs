using System;
using Xamarin.Forms;

namespace AppFom.Helpers
{
    public class ColorManager
    {
        public ColorManager() { }

        public Color UIKitWhite { get { return Color.FromHex("ffffff"); } }

        public Color UIKitPurple { get { return Color.FromHex("a938fe"); } }

        public Color UIKitBlue { get { return Color.FromHex("008fd6"); } }

        public Color UIKitOrange { get { return Color.FromHex("f7a916"); } }

        public Color UIKitGray { get { return Color.FromHex("686868"); } }

        public Color UIKitYellow { get { return Color.FromHex("fce830"); } }

        public Color UIKitRed { get { return Color.FromHex("ff1c1c"); } }

        public Color UIKitGreen { get { return Color.FromHex("75bf43"); } }

    }
}
